    <img src="/images/blog/gutter-overflow/standing-water.gif" alt="Standing water" />
    <p>Proper slope is essential for effective gutter performance. Gutters must be angled slightly toward the downspouts to allow gravity to carry rainwater away from your home efficiently. If after cleaning your gutters have significant standing water, then this is something that should be addressed.</p>

